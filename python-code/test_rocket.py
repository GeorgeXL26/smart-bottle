from machine import Pin, I2C
import ssd1306
import framebuf
import time

i2c = I2C(0, scl=Pin(1), sda=Pin(0), freq=400000)
oled = ssd1306.SSD1306_I2C(128, 64, i2c)

# 32x32 pixel rocket bitmap (1 = white/on, 0 = black/off)
# Row by row, each byte = 8 pixels
rocket = bytearray([
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x03, 0xC0, 0x00,
    0x00, 0x07, 0xE0, 0x00,
    0x00, 0x0F, 0xF0, 0x00,
    0x00, 0x1F, 0xF8, 0x00,
    0x00, 0x1F, 0xF8, 0x00,
    0x00, 0x3F, 0xFC, 0x00,
    0x00, 0x7F, 0xFE, 0x00,
    0x00, 0x7F, 0xFE, 0x00,
    0x00, 0xFF, 0xFF, 0x00,
    0x01, 0xFF, 0xFF, 0x80,
    0x01, 0xFF, 0xFF, 0x80,
    0x03, 0xFF, 0xFF, 0xC0,
    0x03, 0xFF, 0xFF, 0xC0,
    0x07, 0xFF, 0xFF, 0xE0,
    0x0F, 0xFF, 0xFF, 0xF0,
    0x0F, 0xFF, 0xFF, 0xF0,
    0x1F, 0xFF, 0xFF, 0xF8,
    0x1F, 0xFF, 0xFF, 0xF8,
    0x3F, 0xE0, 0x07, 0xFC,
    0x7F, 0xC0, 0x03, 0xFE,
    0xFF, 0xC0, 0x03, 0xFF,
    0x7F, 0xC0, 0x03, 0xFE,
    0x3F, 0xE0, 0x07, 0xFC,
    0x1F, 0xF0, 0x0F, 0xF8,
    0x0F, 0xF0, 0x0F, 0xF0,
    0x07, 0xF0, 0x0F, 0xE0,
    0x03, 0xF0, 0x0F, 0xC0,
    0x01, 0xF0, 0x0F, 0x80,
    0x00, 0xF0, 0x0F, 0x00,
    0x00, 0x70, 0x0E, 0x00,
    0x00, 0x20, 0x04, 0x00,
])

# Create framebuffer from the bitmap
fb = framebuf.FrameBuffer(rocket, 32, 32, framebuf.MONO_HLSB)

# Display on OLED
oled.fill(0)

# Draw rocket in the center of the screen
oled.blit(fb, 48, 8)  # (128-32)/2 = 48

# Text below
oled.text("BLAST OFF!", 30, 48)
oled.text("Smart Bottle", 24, 57)

oled.show()
print("Rocket displayed!")
